#include <iostream>
using namespace std;
int main()
{
    int num1,num2;
    cout<<"Enter first Number.."<<endl;
    cin>>num1;
    cout<<"Enter second Number..."<<endl;
    cin>>num2;
    cout<<num1<<"+"<<num2<<" = "<<num1+num2<<endl;
    cout<<num1<<"-"<<num2<<" = "<<num1-num2<<endl;
    cout<<num1<<"*"<<num2<<" = "<<num1*num2<<endl;
    cout<<num1<<"/"<<num2<<" = "<<num1/num2<<endl;
return 0;
}